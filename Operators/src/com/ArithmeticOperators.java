package com;

public class ArithmeticOperators {
	public static void main(String[] args) {
		 int a = 20;
		 int b = 30;
		 int c = 40;
		 int d = 19;
		 String company = "FLM";
		 String institution = "Edutech";
		 System.out.println(company +"-"+ institution);
		 
		 int addition = a+b;
		 System.out.println("a + b = " + addition);
		 
		 //String + number = String
		 System.out.println("ab"+ a);
		 
		 System.out.println(a + b +"ab");
		 
		 System.out.println("ab" + a + b);
		 System.out.println(a + b + "ab" + c + d);
		 
		 float e = 7.0f; float f = 2.0f;
		 
		 System.out.println(e/f);
		 // 7.0/2.0 = 3.5
		 System.out.println(e%f);
		 // 7.0 % 2.0 = 1.0 // 7%2 = 1
		 System.out.println(a);
		 a = a+2;
		 System.out.println(a);
		 a+=2;
		 System.out.println(a);
		 a-=5;
		 System.out.println(a);
		 a*=5;
		 System.out.println(a);
		 a/=5;
		 System.out.println(a);
		 a%=5;
		 System.out.println(a);


	}

}
 